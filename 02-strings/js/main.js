const myVariable = "Mathematics";

// length property
// returns numbers of character within a string.
console.log(myVariable.length);

// String Methods:
// charAt method => used to find a specific character in a string at the given index position.
console.log(myVariable.charAt(6));

// indexOf method => used to find first occurrence of a specified character within a string.
console.log(myVariable.indexOf("ati"));

// lastIndexOf method => used to find the last occurrence of a specified character within a string.
console.log(myVariable.lastIndexOf("t"));

// slice method => used to extract a portion of a string and return a new string.
console.log(myVariable.slice(2, 5));

// toUpperCase method => used to convert all characters of a string to uppercase.
console.log(myVariable.toUpperCase());

// toLowerCase method => used to convert all characters of a string to lowercase.
console.log(myVariable.toLowerCase());

// toLowerCase method => used to determine whether a string contains a specified value.
console.log(myVariable.includes("div"));
console.log(myVariable.includes("th"));

// split method => used to divide a string into an array of substrings based on the given separator.
console.log(myVariable.split(""));

