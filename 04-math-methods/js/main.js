// Value of PI
console.log(Math.PI);

// trunc method => removes the decimal part of a number and returns the integer part.
console.log(Math.trunc(5));

// round method => used to round value to the nearest integer.
console.log(Math.trunc(3.5));

// ceil method => used to round up the value.
console.log(Math.ceil(5.2));

// floor method => used to round down the value.
console.log(Math.floor(5.9));

// pow method => used to calculate power of a number.
console.log(Math.pow(5, 2));

// min method => used to find the smallest value among a set of numbers.
console.log(Math.min(5, 2, 15, 1, 7, 3));

// max method => used to find the largest value among a set of numbers.
console.log(Math.max(5, 2, 15, 1, 7, 3));

// random method => used to return a random value from 0(included) to 1(excluded).
console.log(Math.random());

